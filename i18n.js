  function getByPath(obj, path) {
    return path.split(".").reduce((acc, part) => (acc && acc[part] !== undefined ? acc[part] : undefined), obj);
  }

  function t(key, lang) {
    const activeLang = SUPPORTED.includes(lang) ? lang : API.lang;
    return getByPath(DICTIONARY[activeLang], key) ?? getByPath(DICTIONARY.en, key) ?? key;
  }

  function applyTranslationsToRoot(root, lang) {
    root.querySelectorAll("[data-i18n]").forEach((el) => {
      const key = el.getAttribute("data-i18n");
      el.textContent = t(key, lang);
    });

    root.querySelectorAll("[data-i18n-html]").forEach((el) => {
      const key = el.getAttribute("data-i18n-html");
      el.innerHTML = t(key, lang);
    });

    root.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
      const key = el.getAttribute("data-i18n-placeholder");
      el.setAttribute("placeholder", t(key, lang));
    });

    root.querySelectorAll("[data-i18n-value]").forEach((el) => {
      const key = el.getAttribute("data-i18n-value");
      const value = t(key, lang);
      if ("value" in el) el.value = value;
      el.textContent = value;
    });

    root.querySelectorAll("[data-i18n-aria-label]").forEach((el) => {
      const key = el.getAttribute("data-i18n-aria-label");
      el.setAttribute("aria-label", t(key, lang));
    });
  }

  function applyTranslations(lang) {
    document.documentElement.setAttribute("lang", lang);
    applyTranslationsToRoot(document, lang);

    document.querySelectorAll("[data-lang-btn]").forEach((btn) => {
      const btnLang = btn.getAttribute("data-lang");
      btn.setAttribute("aria-pressed", btnLang === lang ? "true" : "false");
    });
  }

  function setLanguage(lang, options = {}) {
    const nextLang = SUPPORTED.includes(lang) ? lang : "en";
    API.lang = nextLang;

    if (options.persist !== false) localStorage.setItem(STORAGE_KEY, nextLang);
    applyTranslations(nextLang);

    if (options.dispatch !== false) {
      document.dispatchEvent(new CustomEvent("df:language-changed", { detail: { lang: nextLang } }));
    }
  }

  function initToggle() {
    document.querySelectorAll("[data-lang-btn]").forEach((btn) => {
      btn.addEventListener("click", () => setLanguage(btn.getAttribute("data-lang")));
    });
  }

